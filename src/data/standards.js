// Utility functions for working with standards data
export const standardsAPI = {
    // Get all standards
    getAll: () => JAZZ_STANDARDS,
    
    // Get standard by ID
    getById: (id) => JAZZ_STANDARDS.find(standard => standard.id === id),
    
    // Get standard by title
    getByTitle: (title) => JAZZ_STANDARDS.find(standard => 
        standard.title.toLowerCase() === title.toLowerCase()
    ),
    
    // Search standards
    search: (query) => {
        const searchTerm = query.toLowerCase();
        return JAZZ_STANDARDS.filter(standard => 
            standard.title.toLowerCase().includes(searchTerm) ||
            standard.composer.toLowerCase().includes(searchTerm) ||
            standard.style.toLowerCase().includes(searchTerm) ||
            standard.key.toLowerCase().includes(searchTerm)
        );
    },
    
    // Filter by difficulty
    getByDifficulty: (difficulty) => 
        JAZZ_STANDARDS.filter(standard => standard.difficulty === difficulty),
    
    // Filter by style
    getByStyle: (style) => 
        JAZZ_STANDARDS.filter(standard => standard.style === style),
    
    // Get random standard
    getRandom: () => JAZZ_STANDARDS[Math.floor(Math.random() * JAZZ_STANDARDS.length)],
    
    // Get practice variations
    getPracticeVariations: () => PRACTICE_VARIATIONS,
    
    // Get checklist items
    getChecklistItems: () => PRACTICE_CHECKLIST_ITEMS,
    
    // Generate realbook.site URL
    getRealbookUrl: (standard) => {
        const slug = standard.title.toLowerCase()
            .replace(/['"]/g, '')
            .replace(/\s+/g, '-')
            .replace(/[^a-z0-9-]/g, '');
        return `https://realbook.site/song/${slug}`;
    }
};